# =============================[Tipos de dados inte e float]============================================

# int --> número inteiro

print(11)  # int

# float --> número com ponto flutuante, representa qualquer número

print(11.23455)  # float

#"função" type() mostra o tipo que o python inferiu ao dado

print('1-',  type('siuu')   )

print('2-',  type(-15)   )

print('3-',  type(1.3)   )

print('4-',type(1.333334726424), type(5), type('siu"')   )

 
